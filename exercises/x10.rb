#  Summary : 
#  Can hash values be arrays? Can you have an array of hashes? 

#  Lets find out:

	# hash values as arrays
	hash = {rockband: ['Beatles', 'Santana', 'Led Zepplin']}

	# array of hashes
	arr = [ {rockband: "Beatles"}, {rockband:"Santana"}, {rockband:"Led Zepplin"} ]

	puts hash[:rockband]

	puts arr[2]
